export function getInvestorSummaryZh(annualCashflow) {
  if (annualCashflow > 1000) {
    return "根據您輸入的資料，這項投資物業在未計稅項前，估算可產生正現金流。";
  }
  if (annualCashflow < -1000) {
    return "根據您輸入的資料，這項投資物業在未計稅項前，估算為負現金流。您可重新檢視租金、購入價格、貸款金額、利率或持有成本。";
  }
  return "根據您輸入的資料，這項投資物業在未計稅項前，估算接近收支平衡。";
}

